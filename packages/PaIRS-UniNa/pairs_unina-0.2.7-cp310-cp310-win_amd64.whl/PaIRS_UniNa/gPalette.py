def lighten_palette(palette:QPalette, factor=0):
    """Ritorna una palette scurita del fattore dato (percentuale, default 110%)."""
    new_palette = QPalette()
    color_roles = [
        QPalette.Window, QPalette.WindowText, QPalette.Base, QPalette.AlternateBase, QPalette.ToolTipBase, 
        QPalette.ToolTipText, QPalette.Text, QPalette.Button, QPalette.ButtonText, QPalette.BrightText, 
        QPalette.Link, QPalette.LinkVisited, QPalette.Highlight, QPalette.HighlightedText, QPalette.PlaceholderText,
    ]
    for color_role in color_roles:
        for color_group in [QPalette.Active, QPalette.Inactive, QPalette.Disabled]:
            original_color:QColor = palette.color(color_group,color_role)
            if factor>0:
                new_color = original_color.darker(factor)
            else:
                new_color = original_color.lighter(abs(factor))
            new_palette.setColor(color_group, color_role, new_color)
    return new_palette
