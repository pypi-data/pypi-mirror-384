    @staticmethod
    def get_details_by_component_id(component_id: int) -> dict:
        component_id = int(component_id)
        if component_id in component_cache:
            return component_cache[component_id]
        else:
            print("component_id not in cache, will try fetching from SQL")
            MiniLogger.start("MiniLogger.Component.get_details_by_component_id",
                             object={"component_id": component_id})
            try:
                # We want to get the component details from SQL in all cases (console, MySQL, Logz.io, Kafka)
                # TODO Use DebugMode (but currently there is circular dependency)  # noqa E501
                # if (False):
                # if (DebugMode.is_write_to_sql()):
                if (True):
                    connection = get_connection(schema_name="component")
                    cursor = connection.cursor()
                    GET_COMPONENT_DETAILS_BY_COMPONENT_ID_COMPONENT_NAME_COLUMN_IDX = 0
                    GET_COMPONENT_DETAILS_BY_COMPONENT_ID_SQL_QUERY = \
                        ("SELECT name, component_type, component_category, testing_framework, api_type "  # noqa E501
                         "FROM component.component_view WHERE component_id = %s")
                    cursor.execute(GET_COMPONENT_DETAILS_BY_COMPONENT_ID_SQL_QUERY, (component_id,))
                    result = cursor.fetchone()
                    if not result:
                        # TODO: don't stop if in production, but in any case insert the error to the logger table
                        raise Exception(
                              f"Component {component_id} not found in the component.component_table")
                    component_json = {
                        "component_id": component_id,
                        "component_name": result[GET_COMPONENT_DETAILS_BY_COMPONENT_ID_COMPONENT_NAME_COLUMN_IDX],
                        "component_type": result[1],
                        "component_category": result[2],
                        "testing_framework": result[3],
                        "api_type": result[4]
                    }
                    component_cache[component_id] = component_json
                else:
                    print("is_write_to_sql is False, returning empty component_json")
                    component_json = {}
                return component_json
            except Exception as exception:
                MiniLogger.exception(
                    f"MiniLogger.Component.get_details_by_component_id component_id={component_id}", exception)  # noqa E501
                component_cache[component_id] = {}
